const add = function (x, y) {
  return x + y;
};
const substract = function (x, y) {
  return x - y;
};
const multiply = function (x, y) {
  return x * y;
};
const divide = function (x, y) {
  return x / y;
};
const pi = 3.14;

module.exports = {
  add: add,
  substract: substract,
  multiply: multiply,
  divide: divide,
  pi: pi,
};
